package com.example.desarrollo3.reportape.Adaptadores;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.ArrayList;

/**
 * Created by Dise07 on 30/12/2016.
 */

public class PaginadorAdaptador  extends FragmentPagerAdapter {

    private ArrayList<Fragment> fragments;

    public PaginadorAdaptador(FragmentManager fm, ArrayList<Fragment> fragments) {
        super(fm);
        this.fragments=fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }

    @Override
    public int getCount() {
        return fragments.size();
    }
}
